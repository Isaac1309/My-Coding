
package Server;

public class Operaciones {
     public int sumaNumeros(int a, int b) {
        int res = 0;
        res = a + b;

        return res;
    }

    public int restaNumeros(int a, int b) {
        int res = 0;
        res = a - b;

        return res;
    }
    public int multNumeros(int a, int b) {
        int res = 0;
        res = a * b;

        return res;
    }
    
    public int divNumeros(int a, int b) {
        int res = 0;
        res = a / b;

        return res;
    }
    
}
